package demo3;

public class DaseNum {
	public static void main(String[] args) {
        
		
		boolean b1 = true;
		boolean b2 = false;
		System.out.println("这道题正确吗？" + b1);
		System.out.println("第二道题正确吗？" + b2);

		//数值 分为：整数int 和 小数float
		int in1 = 32;
		System.out.println("全班人数：" + in1);

		// 浮点数 double  float
		double money = 12.4563;
		System.out.println("你本月的薪水----" + money);

		// 字符型 char
		char c1 = 'a';
		System.out.println("字符----" + c1);
		char c2 = 98;
		System.out.println("字符----" + c2);
		char c3 = '\u5fa2';
		System.out.println("字符----" + c3);
        
	}
}
